# Створюємо пустий словник для зберігання фруктів і їх кольорів
fruits = {}

# Додаємо фрукти та їх кольори
fruits['яблуко'] = 'червоний'
fruits['банан'] = 'жовтий'
fruits['киві'] = 'зелене'

# Виводимо всі фрукти та їх кольори
for fruit, color in fruits.items():
    print(f'Фрукт: {fruit}, Колір: {color}')
